#include "Calculator.h"

float Calculator::Add(float aNum, float bNum)
{
	return aNum + bNum;
}

float Calculator::Sub(float aNum, float bNum)
{
	return aNum - bNum;
}

float Calculator::Mul(float aNum, float bNum)
{
	return aNum * bNum;
}

float Calculator::Div(float aNum, float bNum)
{
	return aNum / bNum;
}